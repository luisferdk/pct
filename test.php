          <h2>Description</h2>
          <p>Isla Saona is a 42 square kilometer Caribbean oasis off the southern coast of the Dominican Republic. Originally named "Adamanay" by the Taino Indians, the island was renamed by Christopher Columbus, during his second voyage to the Americas in 1494. The name, "Saona", commemorated his friend Michele da Cuneo, from Savona, Italy.<br />
<br />
Isla Saona is, currently, the most popular excursion offered in the Dominican Republic. The island is the quintessential tropical beach with swaying palm trees, bone white sand and lapping turquoise waters.<br />
<br />
We'll race you out to the island in a speed boat that includes a stop in a waist deep natural swimming pool of transparent waters. If you're lucky you'll see some starfish! At Isla Saona, you'll have many choices: wade in the turquoise waters, visit the open bar, take a nap on a beach hammock, play volleyball, indulge in a typical Dominican lunch of chicken, pork, rice, beans, salad and fruit, or walk the coastline. Most likely, you'll do it all!<br />
<br />
We'll return to the Dominican Republic mainland on a slow moving catamaran with Latin music, dancing and open bar!</p>
